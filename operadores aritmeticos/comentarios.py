# Os operadores aritméticos são usados para realizar cálculos matemáticos em Python.
# Os operadores aritméticos básicos são:
#
# * **+** - Adição
# * **-** - Subtração
# * **\** - Multiplicação
# * **/** - Divisão
#
# Por exemplo, a seguinte expressão soma dois números:
#
# ```python
# a = 1 + 2
# ```
#
# O resultado desta expressão é 3.
#
# Outros operadores aritméticos incluem:
#
# * **%** - Resto da divisão
# * **** - Exponenciação
# * **//** - Divisão inteira


