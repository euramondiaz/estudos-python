# A lista 'valores' é criada contendo valores numéricos.
valores = [2, 3, 42, 5, -1, 2, 4, 0, -6]

# Imprime o valor máximo da lista 'valores' utilizando a função max().
print(max(valores))

# Imprime o valor mínimo da lista 'valores' utilizando a função min().
print(min(valores))

# As variáveis 'a' e 'b' recebem valores numéricos.
a = -5
b = 4

# Imprime o valor absoluto de 'a' utilizando a função abs().
print(abs(a))

# Imprime 'a' elevado à potência 'b' utilizando a função pow().
print(pow(a, b))

# A variável 'c' recebe um valor decimal.
c = 2.79384545

# Imprime 'c' arredondado para 6 casas decimais utilizando a função round().
print(round(c, 6))
